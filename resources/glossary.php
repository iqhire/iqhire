			<dt><a name="">Encryption</a></dt>
			<dd>The act of fact of converting computer data and messages to something incomprehensible by means of a key, so that it can be reconverted only by and authorized recipient holding the matching key.</dd>
			
			<dt><a name="">Enhanced Search Product</a></dt>
			<dd>Any search product a research provider is willing or able to provide which surpasses the depth, level or scope of their default search product. An example of an enhanced search product might be an extended search or unlimited search, where that provider’s default search product is limited to a seven or ten year search. Another example might be the routine inclusion of copies on any criminal records found, where that provider’s default search product does not include such copies. An enhanced search product would not necessarily be provided by default to any given customer—unless that customer specifically requested such and negotiated with their researcher to obtain it. (Compare with default search product.) </dd>
			
			<dt><a name="">End-User</a></dt>
			<dd>The entity—typically an employer in the case of employment screening, a property owner in the case of tenant screening—that originally requests and ultimately receives the qualified search results in the form of a consumer report. </dd>
			
