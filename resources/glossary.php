			<dt><a name="">Predominantly Used Index (PUI)</a></dt>
			<dd>That portion or portions of a county’s index at the Central Court which is commonly considered adequate to use exclusively in performing a Common Search. For example, in a county with three separate criminal index sections—such as: a section of the felony/misdemeanor index on computer which extends 10 years back; another section of the felony/misdemeanor index on microfiche which extends from 10 to 20 years back; and a felony-only index that extends 60 years back—only that 10 year section of the felony/misdemeanor index on computer would constitute the Predominantly Used Index, if indeed it is the section of public index predominately and exclusively used by the local research community to perform a Common Search in that county. Another example would be a county with a computer index that extends only 5 years back; then another microfiche index which extends from 5 to 10 years back; and a card index that extends 10 to 25 years back. The 5 year computer index plus the 5-10 year microfiche index would constitute the Predominantly Used Index, if indeed those are the sections of public index predominately and exclusively used by the local research community to perform a Common Search in that county. </dd>
			
			<dt><a name="">Prescreened Search</a></dt>
			<dd>Any search on a subject that has been prescreened from a greater group of subjects, with the intent being to eliminate from that original group most or all subjects with Negative Results, so that only subjects with Positive Results need be submitted to the provider. If a subject has been prescreened the customer is fully expected to disclose the fact up front. A prescreened search is not to be confused with a salted search, or with a regular search on a subject who has admitted to having a record. (The customer’s inclusion with a regular search request of any additional information on the subject, obtained my means other than prescreening and simply meant to aid the provider in performing their search, would not constitute a prescreened search). </dd>
			
