			<dd>The process of obtaining and reviewing documentation (licensure, certifications, insurance, et cetera) provided by individuals, businesses or organizations. Generally this will include reviewing the information provided by the source and verifying that the information is accurate and complete.</dd>
			
			<dt><a name="">Cryptography</a></dt>
