			<dd>Defined here as the time or date by which a search is estimated to be completed and delivered to the customer. An ETA by definition is not a guarantee, but rather an estimate—particularly in view of the fact that public record agencies can impose access barriers or delays beyond the control of a provider. </dd>
			
