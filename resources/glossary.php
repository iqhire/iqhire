			<dd>Obtaining qualitative information from a past or current employer typically dealing with job performance and/or workplace behavior. Examples of such information include questions about the quality of a job applicant’s work, attendance, leadership potential, areas of improvement, etc. Reference Questions are generally defined under the Federal Fair Credit Reporting Act (FCRA) as an Investigative Consumer Report section 606.</dd>
			
