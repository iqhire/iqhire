			<dt><a name="">Common Search Guideline</a></dt>
			<dd>A formal, written guideline specifically outlining how a common search is to be performed. Such a guideline could be originated by the research provider or the customer. A provider’s Common Search Guideline is equivalent to a description of their default search product for a common search, which, when disclosed, provides their customer with an expectation of how that provider will perform their common search requests. </dd>
			
			<dt><a name="">Conditional Discharge – Non-conviction</a></dt>
			<dd>The defendant has no finding of guilt. The court is discharging him/her from trial on special conditions that they must follow. If they do not abide by these conditions, the discharge may be revoked and the finding may become guilty.</dd>
			
			<dt><a name="">Consent Decree – Conviction</a></dt>
			<dd>This is found in New Mexico. It is designed as a disposition for juvenile cases in which the defendant pleads guilty and is placed on the decree / probation for six months.</dd>
			
			<dt><a name="">Consumer</a></dt>
			<dd>A consumer, for our purposes, is the applicant or employee on whom background information is being gathered. </dd>
			
			<dt><a name="consumer_report">Consumer Report</a></dt>
			<dd><p>A consumer report, for our purposes, is a pre-employment or pre-tenancy background report, prepared by a Consumer Reporting Agency on a consumer.</p>
				
				<p>Official definition:</p>
					<blockquote>
						<p>Any written, oral, or other communication of any information by a consumer
							reporting agency bearing on a consumer's credit worthiness, credit standing, credit capacity,
							character, general reputation, personal characteristics, or mode of living which is used or expected
							to be used or collected in whole or in part for the purpose of serving as a factor in establishing the
							consumer's eligibility for (A) credit or insurance to be used primarily for personal, family, or
							household purposes; (B) employment purposes; or (C) any other purpose authorized under
							section 604 [§ 1681b] of the Fair Credit Reporting Act (FCRA).</p></blockquote>
			</dd>
			
			<dt><a name="consumer_reporting_agency">Consumer Reporting Agency (CRA)</a></dt>
			<dd>Any person which, for monetary fees, dues, or on a cooperative nonprofit basis, regularly engages in whole or in part in the practice of assembling or evaluating consumer credit information or other information on consumers for the purpose of furnishing consumer reports to third parties, and which uses any means or facility of interstate commerce for the purpose of preparing or furnishing consumer reports.</dd>

			<dt><a name="">CRA</a></dt>
