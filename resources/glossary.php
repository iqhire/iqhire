			<dt><a name="">Central Court</a></dt>
			<dd>In counties with multiple courts that include smaller, local, lower level, outlying or remote municipal or justice-of-the-peace courts, the Central Court is considered that court in which felonies and most misdemeanors are adjudicated. The Central Court index may not include all cases that can be found in these smaller local courts, as such courts may not report all convictions to the Central Court—or "County Seat" Court as it is sometimes referred to.</dd>
			
			<dt><a name="">Code of Federal Regulations (CFR)</a></dt>
			<dd>The codification of the general and permanent rules and regulations published by the executive departments and agencies of the Federal Government of the United States.</dd>
			
			<dt><a name="">Collection site</a></dt>
			<dd>A place selected by the employer where employees present themselves for the purpose of providing a urine specimen for a drug test.</dd>
			
			<dt><a name="">Common Name</a></dt>
			<dd>Any name that is found to have a Matching Logic of Partial Name Match Only or Full Name Match Only with at least 5 different subjects listed on any single Predominately Used Index. For example, if the research provider is given a Robert L. Smith to check on an index with no DOBs, and discovers at least five different listings for a Robert Smith with no middle, Robert L. Smith would constitute a Common Name on that index. </dd>
			
