			<dd>a CRA that is engaged in the collection, management, and distribution of public records data via an on-line computerized database.</dd>
			
			<dt><a name="">Dead Docket – Non-conviction</a></dt>
			<dd>Often seen in Fulton, Georgia, there is not enough evidence that shows the defendant is guilty or that he is innocent. The case is set aside. If not brought back up, case is closed.</dd>
			
			<dt><a name="">Default Search Product</a></dt>
			<dd>The search product the research provider will, by default, routinely perform for any given customer unless specifically negotiated with to do otherwise (see enhanced search product). A provider’s rates would typically be based upon their default search products (see default search product rate). </dd>
			
			<dt><a name="">Default Search Product Rate</a></dt>
			<dd>The rate a research provider charges for their default search product.</dd>
			
			<dt><a name="">Deferred judgment</a></dt>
			<dd>Occurs after you admit to all or part of the charges against you but before that plea is entered on the record. You are not sentenced and instead serve a period of probation, deferring judgment. In some states like Maryland, this is called probation before judgment (PBJ). NOTE: The implementation for deferred judgment/deferred prosecution programs vary widely from state to state, and even between courts in the same state.</dd>
			
			<dt><a name="">Deferred prosecution</a></dt>
			<dd>Does not require a guilty plea. You are simply required to serve the probation before you are taken to court on the charges.  NOTE: The implementation for deferred judgment/deferred prosecution programs vary widely from state to state, and even between courts in the same state.</dd>
			
			<dt><a name="">Derogatory Information</a></dt>
			<dd>A result that could be classified as adverse to the subject under FCRA or other applicable laws. </dd>
			
			<dt><a name="">Disposition</a></dt>
			<dd>The court’s final determination or adjudication of a criminal charge. </dd>
			
			<dt><a name="">Disposition Date</a></dt>
			<dd>The date of final adjudication - i.e., either the date of conviction or the date of dismissal or acquittal. </dd>
			
