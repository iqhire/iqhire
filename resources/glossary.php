			<dt><a name="employment_purposes">Employment Purposes</a></dt>
			<dd>The term “employment purposes”, when used in connection with a Consumer Report, means a report used for the purpose of evaluating a consumer for employment, promotion, reassignment or retention as an employee.</dd>
			
