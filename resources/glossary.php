		<dl class="definitions">
			<dt><a name="">Adjudication</a></dt>
			<dd>The ability to automate a judgment based on background screening outcome. This judgment often results in “meets requirements” or “does not meet requirements” outcome. Once adjudication takes place automatic distribution and/or adverse action can occur.</dd>
			
			<dt><a name="">Adjudication Withheld – Non-conviction</a></dt>
			<dd>The court does not give a final judgment regarding the case. The defendant is given probation, a program or community service in which they have a specified amount of time to complete. If the defendant complies, the case may be dismissed, depending on the county/state. If they do not dismiss in that particular county / state, then the disposition remains adjudication withheld and the case is closed. However, if the defendant is found in violation, the case disposition may be changed and the defendant can be found guilty.</dd>
			
			<dt><a name="">Admitted Record</a></dt>
			<dd>A record the subject has admitted to having. Admitted record information can prove valuable for the research provider in conducting a search, and ideally customers would include any such information up front with their search requests as matter of course.</dd>
			
			<dt><a name="adverse_action">Adverse Action</a></dt>
			<dd>The term adverse action means: 1) a denial or cancellation of, an increase in any charge for, or a reduction or other adverse or unfavorable change in the terms of coverage or amount of, any insurance, existing or applied for, in connection with underwriting of insurance; 2) a denial of employment or any other decision for employment purposes that adversely affects any current or prospective employee; or 3) a denial or cancellation of, an increase in any change for, or any other adverse or unfavorable change in the terms of, any license or benefit described in Section 604, Subsection (A), Subsection (3), Subsection (D).</dd>
			
			<dt><a name="">Applicant</a></dt>
			<dd>The subject of the inquiry—a job applicant or current employee who is the subject of an employment verification or reference.</dd>
			
			<dt><a name="">Applicant Tracking System (ATS)</a></dt>
			<dd>Any software program, that manages both an organization’s job posting and data collection (i.e., resume/applications) process to efficiently process prospective applicants.  Generally feature rich to handle various aspects of the hiring cycle.</dd>
			
