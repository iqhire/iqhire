		<h2>Criminal Background Check</h2>

			<h4>County Criminal Record Search</h4>
			<p>A search of court records for each county of residence is conducted. Counties to be searched may be identified by the job application, the social security number trace, or by the employer. The search will yield data on court cases filed, charges filed, conviction data, and case disposition. This is a fundamental search for investigating criminal backgrounds.</p>

			<h4>Statewide Criminal Record Search</h4>
			<p>This search provides a complete picture of an applicant's records at the state reporting level, which may contain data not found at the county level, and encompasses all counties within the state providing a more comprehensive search.. The statewide search offers an additional layer of security to the recruitment process.</p>

			<h4>National Criminal Record Search</h4>
			<p>This nationwide search contains records from more than 40 states and multiple data sources including Departments of Corrections, state courts, law enforcement agencies, traffic courts, county courts, and prison parole and release files. Like the statewide search, the national search casts a wider net, and provides an extra layer of security to the recruitment process.</p>

			<h4>Registered Sex Offender Search</h4>
			<p>This database search is conducted for all applicable states which report and release sex offender data.</p>

			<h4>Federal Criminal Record Search</h4>
			<p>A search of court records in federal judicial districts is conducted. Jurisdictions searched include the applicant's current and previous jurisdictions of residence. Reports will include court, case number, date filed, charges filed, conviction date, and disposition.</p>

			<h4>Federal and State Civil Record Search</h4>
			<p>Federal and state jurisdictions are selected based on the applicant's current and previous addresses. Reports include court, case number, date filed, complaint filed, plaintiff, defendant, date of disposition, and disposition.</p>

			<h4>Global Report</h4>
			<p>A name search is conducted in a database comprised of U.S. and foreign sanctions and watch lists.</p>


